// search-and-select Hilfsfunktion
export async function searchAndSelect(page, { target, value, resultSelector, resultIndex = 0 }) {
  const locator = page.locator(`[data-id="${target['data-id']}"]`).first();
  await locator.scrollIntoViewIfNeeded();
  await locator.fill(value);
  await locator.press('Enter');
  const results = page.locator(resultSelector);
  await results.nth(resultIndex).scrollIntoViewIfNeeded();
  await results.nth(resultIndex).click();
}
